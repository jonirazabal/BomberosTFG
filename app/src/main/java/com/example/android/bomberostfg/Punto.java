package com.example.android.bomberostfg;

public class Punto {
    double mylat;
    double mylong;
    double yourlat;
    double yourlong;

    public Punto(){
    }

    public double getMylat() {
        return mylat;
    }

    public void setMylat(double mylat) {
        this.mylat = mylat;
    }

    public double getMylong() {
        return mylong;
    }

    public void setMylong(double mylong) {
        this.mylong = mylong;
    }

    public double getYourlat() {
        return yourlat;
    }

    public void setYourlat(double yourlat) {
        this.yourlat = yourlat;
    }

    public double getYourlong() {
        return yourlong;
    }

    public void setYourlong(double yourlong) {
        this.yourlong = yourlong;
    }
}
